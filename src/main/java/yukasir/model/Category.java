package yukasir.model;

/**
 *
 * @author Paideia
 */

public class Category {
    private int id;
    private String name;

    public Category(int id, String name) {
        this.id = id;
        this.name = name;
    }

    // Getters and setters (you can generate these if using an IDE like IntelliJ IDEA or Eclipse)
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
