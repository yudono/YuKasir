    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(hitung > -1){
            String invoice = "inv-"+generateRandomString();
            TransactionController controller = new TransactionController();
            transaction.forEach((id,trx)->{
                try {
                    controller.create(invoice,trx.id, trx.quantity, total, hitung);
                } catch (SQLException ex) {
                    Logger.getLogger(Payment.class.getName()).log(Level.SEVERE, null, ex);
                }
            });
            showMessageDialog(null,"Pembayaran berhasil","Success", INFORMATION_MESSAGE);
            
            App app = new App();
            app.setVisible(true);
            
            dispose();
        }else{
            showMessageDialog(null,"Pembayaran anda kurang","Error", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

