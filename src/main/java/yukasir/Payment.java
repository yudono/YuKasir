    private void dibayarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_dibayarKeyReleased
        // TODO add your handling code here:
        NumberFormat numberFormat = NumberFormat.getInstance(Locale.GERMANY);
        int dibayarkan = Integer.parseInt(dibayar.getText());
        hitung = dibayarkan-total;
        bayar.setText("Rp"+numberFormat.format(dibayarkan));
        kembalian.setText("Rp"+numberFormat.format(hitung));
        
        if(hitung < 0){
            uangkurang.setText("Uang anda kurang");
            kembalian.setForeground(Color.red);
        }else{
            uangkurang.setText("");
            kembalian.setForeground(Color.black);
        }
    }//GEN-LAST:event_dibayarKeyReleased


